# declaração de variáveis
cidades = []

# adição de informações na lista cidades
for i in range(5):
    cidade = input(f'Digite o nome da {i+1}ª cidade: ')
    temperatura = float(input(f'Digite a temperatura, em ºC, de {cidade.title()}: '))
    cidades.append([cidade, temperatura])

for cidade, temperatura in cidades:
    fahrenheit = temperatura * 1.8 + 32
    print(f'{cidade.title()} {fahrenheit} ºF')
